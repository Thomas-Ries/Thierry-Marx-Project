<script>
import recettesNavbar from "../components/navbar/recettesNavbar";
import { gsap } from 'gsap';


export default {
    name:"chef",
    
    components : {
        recettesNavbar,
    },

    mounted() {
        this.init(),
        this.anim()
    },

    methods: {
        init() {
            gsap.timeline({
                scrollTrigger: {
                    scrollTrigger: ".decouverte",
                    start: 'center center',
                    end: "bottom top",
                    scrub: true,
                    pin: true,
                }
            })
            .from(".decouverte", {
                opacity: 0,
                delay: 1,
                duration: 10,
            })

            gsap.timeline({
                scrollTrigger: {
                    scrollTrigger: ".inspiration",
                    start: 'center center',
                    end: "bottom top",
                    scrub: true,
                    pin: true,
                }
            })
            .from(".inspiration", {
                opacity: 0,
                duration: 10,
                y: -200,
            })
        },

        anim: function() {
            const hover = document.querySelector(".wide-img");
            const img = document.querySelector(".img-jumbotron");
            const TL = gsap.timeline({paused: true});

            TL.to(img, {delay:.1, css: { 'filter': 'grayscale(0%)','-webkit-filter': 'grayscale(0%)'}, ease: "expo.out"}),

            hover.addEventListener('mouseenter', () => {
                TL.play();
            }),
            hover.addEventListener('mouseout', () => {
                TL.reverse();
            })

        }
    }
}
</script>

